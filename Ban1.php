เทพนิยายญี่ปุ่น Japanese Fairy Tales

เทพนิยายที่มีเสน่ห์ดึงดูดและมีความเป็นสากลซึ่งหาได้ยากยิ่ง ไม่มีข้อจำกัดทางตรรกะหรือทางภูมิศาสตร์ไม่ว่าจะเป็นเรื่องราวของคนธรรมดา พระราชา คนแก่ชราที่นิสัยดีและไม่ดี เจ้าหญิงและนักรบ 
สัตว์ต่างๆ ท้องฟ้าท้องทะเล รวมถึงอาณาจักรของจินตนาการอันไร้ของเขต

หนังสือ "เทพนิยายญี่ปุ่น" เล่มนี้จะทำให้คุณได้สัมผัสกับโลกในจินตนาการไม่ว่าจะเป็นโลกของภูตผี ยักษ์ มังกรทะเล และราชาแห่งท้องทะเล ดังเช่นในเรื่อง "นักรบข้าวหนึ่งถุง" 
ที่ปลาทองจะออกมาร่ายรำและปลาคาร์ปนักดนตรีก็จะบรรเลงเพลงขับกล่อมฮิเดะโยชิ นักรบผู้กล้าหรือในเรื่อง "กระจกเงาแห่งมัตซึยามะ" ที่เล่าถึงบุตรสาวผู้เปลี่ยวเหงา ซึ่งรอดผ่านชะตากรรมของเธอ
มาได้ด้วยความช่วยเหลือจากแผ่นโลหะกลมมันวาว ที่แม่ผู้ล่วงลับมอบไว้ให้ และอีกเรื่อง "แมงกะพรุนกับลิง" ที่อธิบายว่าแมงกะพรุนสูญเสียกระดูกสันหลังของมันได้อย่างไร นอกจากนี้ยังมีเรื่องเกี่ยวกับ
วีรบุรุษ "โมโมทาโร" ซึ่งเกิดมาจากลูกพืชที่ลอยน้ำมาติดริมฝั่ง โดยเรื่องนี้นั้นเป็นตำนานที่เด็กญี่ปุ่นทุกคนต่างคุ้นเคยกันเป็นอย่างดี

แม้สถานที่และตัวละครในแต่ละเรื่องจะมีลักษณะแตกต่างกันออกไป แต่ผลที่เกิดขึ้นจากทุกเรื่องเล่าในหนังสือเล่มนี้นั้นเหมือนกันคือการนำพาผู้อ่านทั้งเด็กและผู้ใหญ่ไปยังชายฝั่งอันลึกลับ 
อาณาจักรมหัศจรรย์ และดินแดนแห่งเทพนิยายซึ่งหนังสือ เทพนิยายญี่ปุ่น เล่มนี้เป็นเสมือนการแนะนำให้รู้จักกับจินตนาการอันล้นเหลือของชาวอาทิตย์อุทัยที่สืบต่อกันมาเป็นระยะเวลายาวนานได้เป็นอย่างดี

